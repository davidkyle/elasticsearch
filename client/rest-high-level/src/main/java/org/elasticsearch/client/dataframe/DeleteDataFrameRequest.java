package org.elasticsearch.client.dataframe;

import org.elasticsearch.action.ActionRequest;
import org.elasticsearch.action.ActionRequestValidationException;
import org.elasticsearch.common.ParseField;
import org.elasticsearch.common.xcontent.ToXContentObject;
import org.elasticsearch.common.xcontent.XContentBuilder;

import java.io.IOException;
import java.util.Objects;

public class DeleteDataFrameRequest extends ActionRequest implements ToXContentObject {
    public static final ParseField ID = new ParseField("id");

    private String id;

    public DeleteDataFrameRequest(String id) {
        this.id = Objects.requireNonNull(id, "[data frame id] must not be null.");
    }

    public String getId() {
        return id;
    }

    @Override
    public ActionRequestValidationException validate() {
        return null;
    }

    @Override
    public XContentBuilder toXContent(XContentBuilder builder, Params params) throws IOException {
        builder.startObject();
        builder.field(ID.getPreferredName(), id);
        builder.endObject();
        return builder;
    }

    @Override
    public int hashCode() {
        return Objects.hash(id);
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }

        if (obj == null || getClass() != obj.getClass()) {
            return false;
        }
        DeleteDataFrameRequest other = (DeleteDataFrameRequest) obj;
        return Objects.equals(id, other.id);
    }
}
